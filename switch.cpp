
#include <iostream> 
using namespace std; 

int main () { 

    int day; 
    cout<<"enter valid day"<<endl;  
    cin>>day; 

    switch (day) {

        case 2: 
            cout<<"tuesday"; 
            break; 
        
        case 3: 
            cout<<"wednesday"; 
            break; 

        case 4:
        cout<<"thursday"; 
        break; 

        case 5: 
        cout<<"friday"; 
        break; 

        case 6: 
        cout<<"saturday"; 
        break; 
        
        default: 
        cout<<"invalid hai"; 

    } 
    return 0; 
}
















